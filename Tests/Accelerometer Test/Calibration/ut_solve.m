function x = ut_solve(U,b,n)

% SYNTAX:
%   x = ut_solve(U,b,n);
%
% DESCRIPTION:
%   Returns U'*x = b
%----------------------------------------------------------------------------------------------
% Copyright (C) 2018 Do Yeou Ku
%----------------------------------------------------------------------------------------------

x(1) = b(1)/U(1,1);
    for k=2:n
        x(k) = (b(k)-x(1:k-1)*U(1:k-1,k))/U(k,k);
    end
end

